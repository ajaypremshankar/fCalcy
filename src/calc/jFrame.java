    private void bSinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSinActionPerformed
        text0="sin("+t1.getText()+")";
        t0.setText(text0);
        val=Double.parseDouble(t1.getText());
        if(radflag)
            text=""+Math.sin(val);
        else
            text=""+Math.sin(Math.toRadians(val));
        t1.setText(text);
        calflag=true;
}//GEN-LAST:event_bSinActionPerformed

