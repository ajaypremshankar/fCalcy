    private void bEqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEqActionPerformed
        double result=0;
        t0.setText("");
        op2=Double.parseDouble(text);

        if(op.equals("bXY"))
        {
            text=""+Math.pow(op1,op2);
            t1.setText(text);
            
        }
        else if(op.equals("+"))
        {
            result=op1+op2;
            text=""+result;
            t1.setText(text);
        }
        else if(op.equals("-"))
        {
            result=op1-op2;
            text=""+result;
            t1.setText(text);
        }
        else if(op.equals("*"))
        {
            result=op1*op2;
            text=""+result;
            t1.setText(text);
        }
        else if(op.equals("/"))
        {
            result=op1/op2;
            text=""+result;
            t1.setText(text);
        }
        else if(op.equals("%"))
        {
            result=op1%op2;
            text=""+result;
            t1.setText(text);
        }
        calflag=true;
    }//GEN-LAST:event_bEqActionPerformed

